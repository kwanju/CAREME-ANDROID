package zangdol.careme.model;

public class Discover {
    private String idx;
    private String discoverDateTime;
    private String discoveredSpot;
    private String description;
    private String speciesCode;
    private String animalSex;
    private String url_picture;
    private String registerDateTime;
    private String userIdx;
    private String matchingShelterIdx;
    private String shelterName;

    public String getIdx() {
        return idx;
    }

    public void setIdx(String idx) {
        this.idx = idx;
    }

    public String getDiscoverDateTime() {
        return discoverDateTime;
    }

    public void setDiscoverDateTime(String discoverDateTime) {
        this.discoverDateTime = discoverDateTime;
    }

    public String getDiscoveredSpot() {
        return discoveredSpot;
    }

    public void setDiscoveredSpot(String discoveredSpot) {
        this.discoveredSpot = discoveredSpot;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getSpeciesCode() {
        return speciesCode;
    }

    public void setSpeciesCode(String speciesCode) {
        this.speciesCode = speciesCode;
    }

    public String getAnimalSex() {
        return animalSex;
    }

    public void setAnimalSex(String animalSex) {
        this.animalSex = animalSex;
    }

    public String getUrl_picture() {
        return url_picture;
    }

    public void setUrl_picture(String url_picture) {
        this.url_picture = url_picture;
    }

    public String getRegisterDateTime() {
        return registerDateTime;
    }

    public void setRegisterDateTime(String registerDateTime) {
        this.registerDateTime = registerDateTime;
    }

    public String getUserIdx() {
        return userIdx;
    }

    public void setUserIdx(String userIdx) {
        this.userIdx = userIdx;
    }

    public String getMatchingShelterIdx() {
        return matchingShelterIdx;
    }

    public void setMatchingShelterIdx(String matchingShelterIdx) {
        this.matchingShelterIdx = matchingShelterIdx;
    }

    public String getShelterName() {
        return shelterName;
    }

    public void setShelterName(String shelterName) {
        this.shelterName = shelterName;
    }
}
